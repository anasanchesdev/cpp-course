#include <iostream>  // Biblioteca com funções de Input e Output

int main(){
    /*
    std: Standard
    C: Character
    Out: Output
    <<: Indica que a informação é saída
    endl: Endline, indica o final de uma string
    */
    std::cout << "Oiii!" << std::endl;
    std::cout << "Tchauu!!" << '\n';
    return 0;
}
